export type 品级 = '普通' | '精品' | '稀有' | '神器' | '史诗' | '传说';

const 品级序: 品级[] = ['普通', '精品', '稀有', '神器', '史诗', '传说'];

export function getGradeByLevel(level: number): 品级 {
  if (level <= 10) return '普通';
  if (level <= 18) return '精品';
  if (level <= 26) return '稀有';
  if (level <= 36) return '神器';
  if (level <= 46) return '史诗';
  return '传说';
}

export const 职业池: string[] = ['前军人', '游荡者', '战士', '医师', '狙击手', '重装', '斥候'];

export function pickRandomProfession(): string {
  return 职业池[Math.floor(Math.random() * 职业池.length)];
}

