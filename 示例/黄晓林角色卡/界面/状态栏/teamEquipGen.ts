const 六维键 = ['力量', '敏捷', '体质', '智力', '感知', '魅力'] as const;

/** 随机六维（每项 8～16），新队友入队用 */
export function randomAbilityScores(): Record<string, number> {
  const out: Record<string, number> = {};
  for (const key of 六维键) {
    out[key] = 8 + Math.floor(Math.random() * 9);
  }
  return out;
}

/** 随机等级（10～35），新队友入队用 */
export function randomLevel(): number {
  return 10 + Math.floor(Math.random() * 26);
}

/** 与状态栏 EXP 条一致：当前等级起始经验 = 50*(等级-1)*等级；新队友入队时 经验值 应等于此值以匹配随机等级 */
export function expForLevel(等级: number): number {
  return 等级 <= 1 ? 0 : 50 * (等级 - 1) * 等级;
}

/** 按等级生成当前/最大 HP（公式约 50+等级*4），新队友入队用 */
export function hpFromLevel(等级: number): { 当前HP: number; 最大HP: number } {
  const 最大HP = Math.max(10, 50 + 等级 * 4);
  return { 当前HP: 最大HP, 最大HP };
}

